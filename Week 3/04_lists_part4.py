"""
LISTS PART 4: SLICING

Topics:
- list[start:end]
- Default values
"""

students = ["JOC", "akanksha", "arun", "harish", "lakshmi", "rajesh"]

# Slice from index 1 to 4
print(students[1:5])

# Entire list
print(students[:])

# From index 3 till end
print(students[3:])

# From start till index 4
print(students[:4])
